# ---------------------------------------------------------------------------
# Immediate alert — called on profile save (send_immediate_reminder)
# ---------------------------------------------------------------------------

def send_immediate_reminder(user_id: str) -> bool:
    """
    Called when a user saves their profile.
    Determines the closest upcoming meal by local server time and sends it.
    Reads meals from users.meal_plan — the same data the dashboard shows.
    """
    logger.info(f"⚡ Sending immediate reminder for user: {user_id}")
    users_col = db["users"]

    try:
        user_doc = users_col.find_one({"_id": ObjectId(user_id)})
        if not user_doc:
            logger.warning(f"⚠️  User {user_id} not found.")
            return False

        if not user_doc.get("reminder_enabled"):
            logger.info(f"⏭️  Reminders disabled for {user_id}.")
            return False

        hour = datetime.now().hour

        # Determine which meal slot is current / upcoming:
        # 00:00–08:59  → today's Breakfast
        # 09:00–12:59  → today's Lunch
        # 13:00–18:59  → today's Dinner
        # 19:00–23:59  → tomorrow's Breakfast (today's dinner already passed at 7 PM)
        if hour < 9:
            meal_type, emoji = "Breakfast", "🍳"
            tomorrow = False
        elif hour < 13:
            meal_type, emoji = "Lunch", "🍛"
            tomorrow = False
        elif hour < 19:
            meal_type, emoji = "Dinner", "🌙"
            tomorrow = False
        else:
            # After 7 PM — dinner has passed, preview tomorrow's breakfast
            meal_type, emoji = "Breakfast", "🍳"
            tomorrow = True

        day_key = _tomorrow_key() if tomorrow else _today_key()

        user_data = {
            "email":         user_doc.get("email"),
            "name":          user_doc.get("name", "there"),
            "meal_plan":     user_doc.get("meal_plan") or {},
            "smtp_email":    user_doc.get("smtp_email"),
            "smtp_password": user_doc.get("smtp_password"),
        }

        meal_name, nutrition = _get_meal(user_data, meal_type.lower(), day_key)
        if meal_name is None:
            logger.warning(f"⚠️  No {meal_type} found for immediate reminder — user {user_id} has no meal plan yet.")
            return False
        day_label = f"Tomorrow ({day_key.title()})" if tomorrow else f"Today ({day_key.title()})"

        subject, body = _build_reminder_body(
            name=user_data["name"],
            day_label=day_label,
            meal_type=meal_type,
            meal_name=meal_name,
            nutrition=nutrition,
        )
        # Personalise subject for profile-update trigger
        subject = f"{emoji} Profile Updated — Your {day_label} {meal_type} · NutriAI"

        return send_email(
            to_email=user_data["email"],
            subject=subject,
            body=body,
            smtp_email=user_data.get("smtp_email"),
            smtp_password=user_data.get("smtp_password"),
        )

    except Exception as exc:
        logger.error(f"❌ Immediate reminder error for {user_id}: {exc}")
        return False


# ---------------------------------------------------------------------------
# Welcome email — no meal content, just onboarding
# ---------------------------------------------------------------------------

def send_welcome_email(user_id: str) -> bool:
    logger.info(f"💌 Sending welcome email for user: {user_id}")
    users_col = db["users"]
    try:
        user = users_col.find_one({"_id": ObjectId(user_id)})
        if not user:
            return False

        subject = "🌊 Welcome to NutriAI!"
        body = (
            f"Hello {user.get('name', 'there')},\n\n"
            f"Welcome to NutriAI! Your account is now active.\n\n"
            f"Complete your profile to generate a personalised 7-day meal plan. "
            f"You will then start receiving breakfast, lunch, and dinner reminders "
            f"with your exact meals from the dashboard.\n\n"
            f"Stay healthy,\n"
            f"— The NutriAI Team"
        )
        return send_email(
            to_email=user["email"],
            subject=subject,
            body=body,
            smtp_email=user.get("smtp_email"),
            smtp_password=user.get("smtp_password"),
        )
    except Exception as exc:
        logger.error(f"❌ Welcome email failed for {user_id}: {exc}")
        return False


# ---------------------------------------------------------------------------
# Scheduler setup
# ---------------------------------------------------------------------------

scheduler = BackgroundScheduler(timezone="Asia/Kolkata")


def start_scheduler():
    if scheduler.running:
        logger.warning("⚠️  Scheduler already running, skipping duplicate start.")
        return

    scheduler.add_job(breakfast_job, "cron", hour=9,  minute=0, id="breakfast_reminder", replace_existing=True)
    scheduler.add_job(lunch_job,     "cron", hour=13, minute=0, id="lunch_reminder",     replace_existing=True)
    scheduler.add_job(dinner_job,    "cron", hour=19, minute=0, id="dinner_reminder",    replace_existing=True)

    scheduler.start()
    logger.info("🚀 Meal reminder scheduler started!")
    logger.info("   ⏰ Breakfast : 09:00 AM IST")
    logger.info("   ⏰ Lunch     : 01:00 PM IST")
    logger.info("   ⏰ Dinner    : 07:00 PM IST")


def stop_scheduler():
    if scheduler.running:
        scheduler.shutdown(wait=False)
        logger.info("🛑 Meal reminder scheduler stopped.")
