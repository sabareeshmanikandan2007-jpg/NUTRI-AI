"""
Meal Reminder Scheduler for NutriAI
====================================
Single source of truth: every email reads meals from the user's stored
meal_plan in MongoDB — the same plan generated by /diet/sync-meal-plan
and shown on the dashboard.

No hardcoded meals.  No GOAL_MEAL_DATABASE fallback.
"""

import logging
from datetime import datetime, timedelta

from apscheduler.schedulers.background import BackgroundScheduler
from bson import ObjectId

from ..database.db import db
from ..utils.email import send_email

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Day helpers
# ---------------------------------------------------------------------------

def _today_key() -> str:
    """Return today's day name as a lowercase string, e.g. 'saturday'."""
    return datetime.now().strftime("%A").lower()


def _tomorrow_key() -> str:
    """Return tomorrow's day name as a lowercase string, e.g. 'sunday'."""
    return (datetime.now() + timedelta(days=1)).strftime("%A").lower()


# ---------------------------------------------------------------------------
# Fetch all reminder-enabled users with their stored meal plan
# ---------------------------------------------------------------------------

def _get_reminder_users() -> list:
    """
    Return a list of dicts for every user who has reminder_enabled=True.
    Each dict contains email, name, smtp credentials, and their full
    meal_plan as stored by /diet/sync-meal-plan.
    """
    users_col = db["users"]
    reminder_users = []

    try:
        for user in users_col.find({"reminder_enabled": True}):
            email = user.get("email")
            if not email:
                continue

            reminder_users.append({
                "id":            str(user["_id"]),
                "email":         email,
                "name":          user.get("name", "there"),
                "meal_plan":     user.get("meal_plan") or {},   # keyed by lowercase day name
                "smtp_email":    user.get("smtp_email"),
                "smtp_password": user.get("smtp_password"),
            })

    except Exception as exc:
        logger.error(f"❌ Error fetching reminder users: {exc}")

    return reminder_users


# ---------------------------------------------------------------------------
# Meal lookup — reads from the stored plan, never hardcoded
# ---------------------------------------------------------------------------

def _get_meal(user: dict, meal_type: str, day_key: str) -> tuple[str | None, dict]:
    """
    Return (meal_name, nutrition_dict) for the given meal_type on day_key.
    meal_type is one of 'breakfast', 'lunch', 'dinner'.
    day_key is a lowercase day name, e.g. 'saturday'.

    Returns (None, {}) if the plan is missing or incomplete — callers should
    skip sending the email rather than sending a "Meal not set" message.
    """
    meal_plan: dict = user.get("meal_plan") or {}
    day_plan  = meal_plan.get(day_key) or {}

    meal_name = day_plan.get(meal_type) or ""
    nutrition  = day_plan.get("nutrition") or {}

    if not meal_name:
        logger.warning(
            f"⚠️  No {meal_type} found for {user['email']} on {day_key}. "
            "Skipping reminder — has sync-meal-plan been called for this user?"
        )
        return None, {}

    return meal_name, nutrition


# ---------------------------------------------------------------------------
# Email body builder
# ---------------------------------------------------------------------------

def _build_reminder_body(
    name: str,
    day_label: str,          # e.g. "Today (Saturday)" or "Tomorrow (Sunday)"
    meal_type: str,          # "Breakfast" | "Lunch" | "Dinner"
    meal_name: str,
    nutrition: dict,
) -> tuple[str, str]:
    """
    Returns (subject, plain_text_body).
    """
    emoji_map = {"Breakfast": "🍳", "Lunch": "🍛", "Dinner": "🌙"}
    emoji = emoji_map.get(meal_type, "🍽️")

    cal   = nutrition.get("calories", 0)
    prot  = nutrition.get("protein", 0)
    carbs = nutrition.get("carbohydrates", 0)
    fat   = nutrition.get("fat", 0)

    subject = f"{emoji} {day_label}'s {meal_type} Reminder — NutriAI"

    nutrition_block = ""
    if cal:
        nutrition_block = (
            f"\n📊 Nutrition:\n"
            f"   🔥 Calories : {cal} kcal\n"
            f"   💪 Protein  : {prot} g\n"
            f"   🌾 Carbs    : {carbs} g\n"
            f"   🥑 Fat      : {fat} g\n"
        )

    body = (
        f"Hello {name},\n\n"
        f"{day_label}'s {meal_type.lower()} is:\n\n"
        f"  {emoji}  {meal_name}\n"
        f"{nutrition_block}\n"
        f"Stay consistent and enjoy your meal! 💚\n"
        f"— NutriAI"
    )

    return subject, body


# ---------------------------------------------------------------------------
# Scheduled job functions
# ---------------------------------------------------------------------------

def breakfast_job():
    logger.info("🍳 Running BREAKFAST reminder job…")
    today = _today_key()
    users = _get_reminder_users()
    sent  = 0
    skipped = 0

    for user in users:
        try:
            meal_name, nutrition = _get_meal(user, "breakfast", today)
            if meal_name is None:
                skipped += 1
                continue
            subject, body = _build_reminder_body(
                name=user["name"],
                day_label=f"Today ({today.title()})",
                meal_type="Breakfast",
                meal_name=meal_name,
                nutrition=nutrition,
            )
            if send_email(
                to_email=user["email"],
                subject=subject,
                body=body,
                smtp_email=user.get("smtp_email"),
                smtp_password=user.get("smtp_password"),
            ):
                sent += 1
        except Exception as exc:
            logger.error(f"❌ Breakfast reminder failed for {user.get('email')}: {exc}")

    logger.info(f"✅ Breakfast reminders sent: {sent}/{len(users)} (skipped {skipped} — no meal plan)")


